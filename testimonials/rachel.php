<p style="clear: both">
<br />
<span class="green_text">Question:</span> How long ago were you diagnosed with breast cancer? 

</p>
<p>
<span class="green_text">Rachel:</span> I was diagnosed in February 2006. 
</p>
<p>
<span class="green_text">Question:</span> How did the consequent treatment phase affect you physically/emotionally? 
</p>
<p>
<span class="green_text">Rachel:</span> I first had surgery then chemotherapy and radiotherapy, which lasted until September last year. Physically it left me ill and exhausted, which made looking after my daughter who was one at the time, very challenging. I put on 8 kilos just through the total inactivity and the affect it has on your metabolism. Emotionally, it was very difficult at the start, to come to terms with being told I had a life threatening illness. It was also very hard to see what the treatment was doing to my body and my overall appearance, by the end of it, my self esteem was at an all time low. 
</p>
<p>

<span class="green_text">Question:</span> How did you decide what course of action you would take to reclaim your health and strength? 
</p>
<p>
<span class="green_text">Rachel:</span> During chemotherapy, I just had to have something to look forward to at the end of my treatment. I hated the way I felt and the way I looked, I really had to reclaim my life. That was when I contacted Donna, I first met her 5 months before I finished treatment and I was so excited that I booked my first session for the same day I finished radiotherapy. 
</p>
<p>
<span class="green_text">Question:</span> What have you found to be the most difficult part of transforming your body? 
</p>
<p>
<span class="green_text">Rachel:</span> The hardest part was finding the energy to turn up to each training session, but when I started to see and feel the results, I couldn&#39;t wait to get to the next one. 

</p>
<p>
... the most exciting? 
</p>
<p>
Last Mother&#39;s Day when I was in the middle of chemo, I did the 4km walk in the Mother&#39;s Day Classic which helps to raise funds for breast cancer research. It took all my energy to finish and I was completely exhausted. This year at the Mother&#39;s Day Classic, I did the 8km run and I was so excited that I followed it with the 4km walk. 
</p>
<p>
...the most unexpected? 
</p>
<p>
I can&#39;t believe I&#39;m running, the furthest I&#39;ve run is 9km so far. I&#39;ve got this new sense of determination and not giving up, it&#39;s the best feeling. 

</p>
<p>
<span class="green_text">Question:</span> What sort of exercise/eating plans have you done in the past and how does this compare with what you are currently doing? 
</p>
<p>
<span class="green_text">Rachel:</span> I&#39;ve been on diets and exercise programs before which included going to the gym, but it&#39;s always been short term goals. My life has been transformed? since I met Donna. She&#39;s taught me all about believing in yourself and that it&#39;s really not hard to eat properly and just to keep moving. 
</p>
<p>
<span class="green_text">Question:</span> Where do you find the drive and motivation to continue to achieve such amazing results? 

</p>
<p>
<span class="green_text">Rachel:</span> Having had a life threatening illness, it has given me the opportunity to look at my life and myself differently. I really feel having gone through what I did and to come out the other side doing what I&#39;m doing means that anything and everything is possible. Nothing is too hard anymore! Plus having such amazing friends and family keeps me determined to stay fit and healthy. 
</p>
<p>
<span class="green_text">Question:</span> In hindsight, is there anything you would have done differently either leading up to, or during this amazing transformation? If so, what would that be? 
</p>
<p>
<span class="green_text">Rachel:</span> If I look back to where I started with Donna 7 months ago, the main thing I would change is having more confidence in myself from the beginning. Confidence that I can achieve the results and do the things I have done. 
</p>

<p>
<span><span class="green_text">Question:</span></span> Finally, what advice would you give to someone who is now going through a similar experience? 
</p>
<p>
<span><span class="green_text">Rachel:</span></span> Being diagnosed with a life threatening illness is not the end, it can be just the beginning. You get your life back and it doesn&#39;t have to take years. I still have some fears and anxieties about the future, but now I see it as an opportunity to do things different. Anything and everything is possible! 
</p>

