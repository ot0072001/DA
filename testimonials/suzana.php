<p>
<span class="green_text">Question:</span> Can you give us a brief history of your diet &amp; lifestyle before starting with our program? 
</p>
<p>
<span><span class="green_text">Suzana:</span></span> I&#39;ve been a classic yo-yo dieter over the last 20 years. I&#39;ve lost weight and put on weight many times. A few years ago I sustained a knee injury which prevented me from exercising and instead of seeking help I did nothing. My diet consisted of copious amounts of sugar, bread, pasta, rice, heavy sauces, soft drinks, coffee and every meal <strong>always</strong> finished with either cake, biscuits, ice cream or chocolate. Chocolate for breakfast was not unusual. I was constantly bloated, dehydrated, my health suffered and the weight kept climbing. 
</p>

<p>
<span class="green_text">Question:</span> Can you describe your diet &amp; lifestyle now? 
</p>
<p>
<span class="green_text">Suzana:</span> Now I make it a priority to make the best food choices. My diet consists of a variety of fresh fruit and vegetables, lean protein, nuts, cheese and water, water, water. Healthy food doesn&#39;t need to be boring. Get creative... experiment with herbs and spices and try different fruits and vegetables. I thought giving up bread would be the hardest thing in the world to do but to my surprise it actually wasn&#39;t. Once you&#39;re open to other food possibilities it&#39;s not as hard as you imagine it to be. I have so much energy and I couldn&#39;t be happier 
</p>

<p>
<span class="green_text">Question:</span> Can you give us a brief history of your physical activity before following Donna Aston's principles? 
</p>
<p>
<span class="green_text">Suzana:</span> I was a member of a gym about 15 years ago although that lasted only a few months. Since then I&#39;ve mainly done walking. Over the last 5 years I&#39;ve done no form of exercise. I joined a gym a couple of months before training at Donna Aston but the lack of guidence and knowledge was really frustrating. There are so many &#39;diet&#39; books out there I didn&#39;t know where to turn to or what to believe. Donna&#39;s no-nonsense common sense approach really appealed to me. 
</p>

<p>
<span class="green_text">Question:</span> Can you describe your training/physical activity and dietary changes now? 
</p>
<p>
<span class="green_text">Suzana:</span> Instead of driving to work I now park 60 minutes away and walk to work or ride my bike everyday. No excuses. If it rains I simply bring an umbrella. Previously I couldn&#39;t job for more than 1 minute and just last weekend I attempted my first jogging session and I made it to the 30 minute mark without stopping. I also do 2-3 strength resistance sessions per week. My knee injury is no longer an injury. I have strengthened them and I can now do lunges and squats. 
</p>
<p>
<span class="green_text">Question:</span> Do you have any comments you'd like to make that may inspire other? 
</p>
<p>

<span class="green_text">Suzana:</span> Change begins with YOU. Stop making excuses (you know which ones they are!) and start making a commitment to change the way you eat and to start exercising. You don&#39;t need to kill yourself exercising and run a marathon. A brisk 30 minute walk is plenty. Park further away from work and walk, get off at an earlier train station and walk, walk to the shops, walk on your lunch break. It&#39;s not about finding the time but making the time. I have learnt so much about food and exercise and now I have the tools to sustain this for life. I&#39;ve replaced bad habits with a whole lot of good ones. As a result I&#39;ve lost 18kg and dropped my body fat from 31% to 14%. Yes... it&#39;s hard work and yes... it&#39;s not always easy but the results you get are definitely worth it. I promise you! 
</p>

