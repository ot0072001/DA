	<p>
<span class="green_text">Question: </span>Can you give us a brief history of your diet/lifestyle before starting program?<br />
<br />
<span class="green_text">Louise: </span>Basically, everything and everybody else was No.1 priority in my life and I was much further down the list! I really wasn't a priority in my own life. This resulted in much neglect of 'me' by 'me' &ndash; eating erratically, overeating when I did eat, eating at odd times. Lot's of 'supersize' options were order of the day &ndash; large coffee's, large glasses of wine.........I mean, I deserved it, didn't I?! I was constantly feeling like I was on catch&ndash;up with everything and more often than not, tired and a bit 'blah'! 
</p>
<p>
<span class="green_text">Question: </span>Can you describe your diet and lifestyle now?<br />
<br />
<span class="green_text">Louise: </span>Wake&ndash;up time arrived last August when I realised that the only person who could 'make me better' was me. So I took my first tentative steps towards my new priority (I was tentative as I was one of those people who would often jump onto the latest idea, go for it 100% and lose interest within a few months &ndash; I decided that this time I was looking for a long&ndash;term, lifestyle change). Now, 8 months in, I'm more than 10 kilos lighter and have way more energy and consequently am much better at dealing with the rollercoaster that is everyday life! Educating myself through the Donna Aston principals as to the right food along with regulating my food has been key to this result along with the realisation that a daily walk is incredibly good for the soul as well as the physique! 
</p>
<p>

<span class="green_text">Question: </span>Can you give us a brief history of your physical activity before Donna's principals?<br />
<br />
<span class="green_text">Louise: </span>I went through 'bouts' of exercise, nothing was ever that regular and so it was very much 100% or nothing. It always felt hard, a struggle. It was inconsistent and often disappointing with its results, or lack of. 
</p>
<p>
<span class="green_text">Question: </span>Can you describe your training/physical activity/dietary changes now?<br />
<br />
<span class="green_text">Louise: </span>Exercising is just part of my everyday life. During my training sessions I want to and enjoy working hard but the stuff I do by myself during the rest of the week is far more relaxed and includes rather lovely excursions such as running along the beach, walking in parks, yoga (I can now touch my toes &ndash; hurrah!) after a stressful day &ndash; it's 'me' time where I can 'get out my own head' for a bit and just be. I'm also up for new challenges with Nikii's running club and am looking forward to taking part in the Sydney City to Surf in August. 
</p>
<p>
<span class="green_text">Question: </span>Do you have any other comments or words of inspiration for others?<br />

<br />
<span class="green_text">Louise: </span>Have an awareness, honesty and lightness with yourself and your approach to changing your lifestyle for the better. Every day is a new day with new challenges, just keep moving forward. With DA principals it's very easy to identify where you are going wrong and the team support you 110% through this process too. I also think it really helps to regularly reassess your goals and readjust them where necessary &ndash; things in life change daily, so will you and so will your goals. 
</p>
