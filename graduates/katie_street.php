<span class="green_text">Why you became a Donna Aston Certified Fitness Trainer:</span> <br />
As a nurse I realise chronic illness is not just a part of growing older; many illnesses associated with aging are preventable. Many of the patients in our intensive care and hospital beds are there because of poor lifestyle choices. Helping people make good health choices will result in a better future for them, avoiding some preventable chronic illnesses that result in pain and poor quality of life. Now, as a personal fitness trainer I can make positive changes in the lives of my clients.
</p>
